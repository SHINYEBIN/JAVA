package kr.co.ezen.variable;

public class A_variable {
	
	public void declareVariable() {
		//1. 논리형(true, false)
		boolean isTrue;
		
		// 2. 숫자형
		// 2-1. 정수형
		byte bNum; //1 byte
		short sNum; //2 byte
		int iNum; //4 byte
		long lNum; //8 byte
		// 2-2. 실수형
		float fNum; //4 byte
		double dNum; //8 byte
		// 3.문자형
		char ch; //2 byte 0~65536
		
		// 4.문자열 
		String str;
		
		isTrue=true;
		bNum=1;
		sNum=2;
		iNum=4;
		lNum=8L;
		fNum=4.0f;
		dNum=8.0;
		
	}
	
 }
